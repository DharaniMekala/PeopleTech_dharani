package W2D1Assignment;
public class series {
	public static void main(String[] args) {
		int star=0;
 		for(int i=1;i<=20;i++)
 		{
				if(i<=10)
				{
	             star++;
				}
				else
	             star--;
		for(int j=1;j<=star;j++)
	
		{
			System.out.print("* ");
		
    }
 		System.out.println(" ");
}
}
}
